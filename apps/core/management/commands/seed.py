class Command(BaseCommand):
    help = "Seed initial data: SiteSettings, ServicePages, Features, FAQs"

    def handle(self, *args, **options):
        settings = SiteSettings.load()
        if not settings.email:
            settings.email = "hello@douit.com.ua"
            settings.phone = "+380 XX XXX XX XX"
            settings.telegram_url = "https://t.me/douit"
            settings.whatsapp_url = "https://wa.me/380XXXXXXXXX"
            settings.save()
            self.stdout.write(self.style.SUCCESS("SiteSettings seeded"))

        for data in SERVICE_DATA:
            features = data.pop("features")
            faqs = data.pop("faqs")
            page, created = ServicePage.objects.update_or_create(
                slug=data["slug"], defaults=data
            )
            if created:
                self.stdout.write(self.style.SUCCESS(f"ServicePage: {page.slug}"))
            for i, (title, text, icon) in enumerate(features):
                Feature.objects.get_or_create(
                    page=page, title=title,
                    defaults={"text": text, "icon_name": icon, "order": i},
                )
            for i, (q, a) in enumerate(faqs):
                FAQ.objects.get_or_create(
                    page=page, question=q,
                    defaults={"answer": a, "order": i},
                )

        for i, data in enumerate(TESTIMONIAL_DATA):
            _, created = Testimonial.objects.get_or_create(
                name=data["name"],
                defaults={**data, "order": i},
            )
            if created:
                self.stdout.write(self.style.SUCCESS(f"Testimonial: {data['name']}"))

        self.stdout.write(self.style.SUCCESS("Seed complete"))
