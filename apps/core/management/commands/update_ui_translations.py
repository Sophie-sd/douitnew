def _parse_po(text: str) -> list[tuple[str, str, str]]:
    """Return list of (comment, msgid, msgstr) tuples from .po text."""
    entries = []
    pattern = re.compile(
        r'((?:#[^\n]*\n)*)'          # optional comment lines
        r'msgid\s+"((?:[^"\\]|\\.)*)"\s+'
        r'msgstr\s+"((?:[^"\\]|\\.)*)"',
        re.DOTALL,
    )
    for m in pattern.finditer(text):
        entries.append((m.group(1), m.group(2), m.group(3)))
    return entries


def _update_po_file(po_path: Path, translations: dict[str, str], dry_run: bool) -> int:
    """Update msgstr values in .po file. Returns count of changes."""
    text = po_path.read_text(encoding="utf-8")
    changes = 0

    for msgid, msgstr in translations.items():
        # Escape special chars for regex
        escaped = re.escape(msgid)
        pattern = re.compile(
            r'(msgid\s+"' + escaped + r'"\s+msgstr\s+)"[^"]*"',
            re.DOTALL,
        )
        replacement = r'\g<1>"' + msgstr.replace("\\", "\\\\").replace('"', '\\"') + '"'
        new_text, n = pattern.subn(replacement, text)
        if n:
            text = new_text
            changes += n
        else:
            # msgid not present — append at end
            entry = f'\nmsgid "{msgid}"\nmsgstr "{msgstr}"\n'
            if not dry_run:
                text += entry
            changes += 1

    if not dry_run:
        po_path.write_text(text, encoding="utf-8")

    return changes


class Command(BaseCommand):
    help = "Update Russian (.po) translations for all UI strings and recompile"

    def add_arguments(self, parser):
        parser.add_argument(
            "--dry-run",
            action="store_true",
            help="Show what would change without writing files",
        )

    def handle(self, *args, **options):
        dry_run = options["dry_run"]
        from django.conf import settings
        po_path = Path(settings.BASE_DIR) / "locale" / "ru" / "LC_MESSAGES" / "django.po"

        if not po_path.exists():
            self.stderr.write(self.style.ERROR(f".po file not found: {po_path}"))
            sys.exit(1)

        if dry_run:
            self.stdout.write(self.style.WARNING("DRY RUN — no files will be changed\n"))

        changes = _update_po_file(po_path, UI_TRANSLATIONS, dry_run)
        self.stdout.write(f"  Entries updated/added: {changes}")

        if not dry_run:
            self.stdout.write("  Compiling messages...")
            result = subprocess.run(
                [sys.executable, "manage.py", "compilemessages"],
                capture_output=True,
                text=True,
            )
            if result.returncode == 0:
                self.stdout.write(self.style.SUCCESS("Done. Translations compiled."))
            else:
                self.stderr.write(self.style.ERROR("compilemessages failed:"))
                self.stderr.write(result.stderr)
        else:
            self.stdout.write(self.style.SUCCESS("Dry run complete. Run without --dry-run to apply."))
